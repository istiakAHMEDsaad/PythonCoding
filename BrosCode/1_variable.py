# Variable = A container for a value (string, integer, float, boolean)
#            A variable behaves as if it was the value it contains

# String
first_name = "Jonny"
print(f"Hola {first_name}")
fav_food = "Pizza"
print(f"Your favourite food is {fav_food}\n")

# Integers
age = 45
print(f"Your age is {age}\n")

# Float
price = 10.99
print(f"Apple price is ${price}\n")

# Boolean
is_student = True
print(f"Are you a student?: {is_student}\n")
